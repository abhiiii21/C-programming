#include<stdio.h>
void main()
{
    int a;
    float b;
    char abhi;
    printf("Enter character value\n");
    scanf("%c",&abhi);
    printf("Enter an integer value\n");
    scanf("%d",&a);
    printf("Enter a decimal value\n");
    scanf("%f",&b);
    printf("Entered character value is %c\n",abhi);
    printf("Entered integer value is %d\n",a);
    printf("Entered decimal value is %f",b);
}